    private void name_fldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_name_fldKeyTyped
        if (name_fld.getText().isEmpty()) {
            add_btn.setEnabled(false);//عشان نتاكد اذا مافي بيانات في الحقل عشان نقفل الزر
        } else {
            add_btn.setEnabled(true);//عشان نتاكد اذا في بيانات في الحقل عشان نشغل الزر حق الاضافة
        }
        char input = evt.getKeyChar();
        if (!(input < '0' || input > '9') && input != '\b') {//عشان نتاكد انو يكتب حروف فقط في الاسم
            evt.consume();
            JOptionPane.showMessageDialog(this, "لايجب ان يحتوي الاسم على ارقام !");
        }


    }//GEN-LAST:event_name_fldKeyTyped

