    private void student_info_tblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_student_info_tblMouseClicked

        model = (DefaultTableModel) student_info_tbl.getModel();
        int selected = student_info_tbl.getSelectedRow();
        int ID = (int) model.getValueAt(selected, 5);
        String name = model.getValueAt(selected, 4).toString();
        name_fld.setText(name);
        String age = model.getValueAt(selected, 3).toString();
        age_fld.setText(age);
        String phone = model.getValueAt(selected, 2).toString();
        phone_fld.setText(phone);
        String level = model.getValueAt(selected, 1).toString();
        class_fld.setSelectedItem(level);
        String gender = model.getValueAt(selected, 0).toString();
        gender_fld.setSelectedItem(gender);

        //just to make the app beauty !
        delete_btn.setEnabled(true);
        update_btn.setEnabled(true);
        id = ID;
        tempName = name;
        gndr = gender;
    }//GEN-LAST:event_student_info_tblMouseClicked

