    private void readDatafromLinkedList() {
        increament = 0;
        try {
            int id = 0;
            model = (DefaultTableModel) student_info_tbl.getModel();

            for (Student_Data looper : studData.stuData) {
                int index = studData.stuData.indexOf(looper);
                String name = looper.getName();
                String age = looper.getAge();
                String phone = looper.getPhone();
                String lvl = looper.getLevel();
                String gender = looper.getGender();
                model.setRowCount(increament);
                model.addRow(new Object[]{gender, lvl, phone, age, name, index});
                increament++;
            }
        } catch (Exception e) {
            System.out.println("reading error = " + e);
        }
    }

